def interpolate(x, y):
    lon = 85.31902581037878 + x * \
        (85.31912639321683 - 85.31902581037878) / 9 + \
        y * (85.3190807956637 - 85.31902581037878) / 18
    lat = 27.68230559651296 + x * (27.682281844553444 - 27.68230559651296) / \
        9 + y * (27.682466515902902 - 27.68230559651296) / 18
    return [lon, lat]
